     The look of fine lines and wrinkles can be improved as cell turnover is increased and damaged skin cells are shed.
      Various concentrations and combinations of peel solutions also prove to be effective for the improvement of mild
       acne by stripping away plugs of keratin, oil, and dirt that often lead to outbreaks.</p><br><p>
       This course is for practitioners who are new to Chemical Peels and we will teach you how to diagnose skin conditions
