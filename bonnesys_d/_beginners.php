   <h2> Beginner (Course Outline)</h2>


<h3>Beginners (Beauty Specialist Diploma)</h3>

<p>These comprehensive beauty courses are for those wishing to work in the beauty Therapy Industry. The 5 beauty Modular courses are as follows;
</p>

<h4>Swedish Body Massage</h4>

<p>After studying human anatomy and physiology, the classic deep-tissue Swedish body massage movements will be taught. Working in pairs, you will both give and receive massage to fully appreciate the effects of the different techniques. You will mostly use oil, but will also be shown other traditional methods</p>

<h4>Manicure and Pedicure</h4>

<p>These beauty lessons will teach all the maintenance and care techniques for the nails and skin of the hands and feet. You’ll learn how to apply single and multi-layer varnishing as well as American and French manicure and pedicure techniques. The theory includes an in-depth study of the structure of the nail as well as the anatomy and physiology of the rest of the lower leg and forearms.</p>

<h4>Waxing Techniques</h4>

<p>During these beauty lessons you will learn how to remove hair using hot bees wax, cream wax and warm organic wax. All areas of the face and body will be worked on. The theory in these beauty courses includes the hair growth cycle.
</p>

<h4>Facial Skin Care Techniques</h4>

<p>You will learn how to Analyze, cleanse, Balance/tone the skin, then the process of Steaming and extraction. You will also learn the Selection, mixing and application of different types of face masque, the full facial massage routine, Client counseling/consultation techniques, Skin Diseases & Disorders, Skin Types and Conditions</p>

<h4>Cosmetic Make-up</h4>

<p>This module will give you training in professional make-up application up to bridal and ‘special occasion’ level. Beauty lessons include training in eyebrow shaping, the application of individual and strip false eyelashes will complete your make-up skills.
</p>


<p>Our beauty courses also teach the Basic Anatomy, Physiology of the skin and some major systems of the human body: (Lymphatic, Cardiovascular and Respiratory Systems), contra-indications to all treatments, client counseling / consultation techniques, product sales, salon reception skills and management duties. You will be assessed for both practical skills and theory knowledge throughout each beauty therapy module.</p>

<h4>Beauty Specialist Diploma ENTRY REQUIREMENTS</h4>

<p>The minimum age for entry to our beauty courses is 15 years. There is no upper age limit to study these beauty lessons.
No previous beauty therapy experience required, although a good general education is essential for our demanding beauty courses.
An excellent level of English is required for this beauty therapy diploma. You must be able to speak, read, write and understand English fluently.
