       1.Signature Hammam Ritual © 60min<br><br>
An age-old Middle Eastern bathing ritual which is deeply nourishing and intensely cleansing to
rejuvenate, eliminate toxins and stimulate circulation. Beginning with a black soap cleanse whilst in the hammam, before a full body traditional exfoliation with kessa mitt. Then the signature blend of melted shea butter and fragranced body oil is warmed for a full body deep-relaxation massage.<br>
							          For optimum results carry out during or after hammam.<br><br><br>

2.Deep-Cleansing Rassoul Ritual © 60min	<br><br>
Pure rassoul lava clay, known for its powerful purifying properties, is warmed with a luscious
honey, rose and ginger mask and melted shea butter. The body is enveloped in this highly
effective warm wrap which removes impurities, conditions, smoothes and restructures the skin’s tissue leaving skin noticeably radiant and beautifully soft. <br>
							        For optimum results carry out during or after hammam.<br><br>

3. Journey to Neezebah 2hr	<br><br>
The ritual begins with a gentle exfoliation using fine ingredients – from the ancient and
mysterious ‘spice route’ - fine grains of almond, apricot, ginger and patchouli. Whilst laying on a warm bed surrounded by candle lights, the body is cocooned in an exotic blend of shea-butter and oil. Shea butter is used to encase the body and the shirodara then drizzles rose water and oil blend onto the body. The bowl dances over the body, immersing all the senses in a feeling of absolute relaxation and wellbeing. The tea and cardamon infused poultices are used to massage releasing heat to further relax. The whole process generates a harmonious flow of energy and a profound sense of peace and gentle stillness – conveying a spiritual message of Irlo- Neezebah.<br>
					                          Ideally the ritual will be pre-ceded by a hammam session.<br><br>



4. Skin Softening Signature © 90 min	<br>	<br>
Transport yourself to the fragrant souks with this truly Arabian experience and full top to toe
treatment. Using all the traditional techniques as used in the Parisian Spas, the body and face is deeply cleansed and exfoliated using the black soap and eucalyptus oil with kessa mitt. Then a warm signature blend is drizzled over the body for a full deep-relaxing massage. A skin
renewing face, neck and scalp massage concludes this delightful experience and skin is left
looking youthful and positively radiant. <br>	        For optimum results carry out during or after hammam.<br><br>

5.Smoothing Salt Glow © 30 min	<br><br>
A full body exfoliation using fragranced mineral salts in oil to remove dead skin and boost
circulation. The green tea & ginger is a great fragrance for this to make it a truly energising and circulation boosting experience. An essential treatment for healthy, silky-smooth skin.<br>
 						For optimum results carry out treatment during or after hammam.<br><br>

6. The Ritual of Relaxation 60min<br><br>
A dreamy delight that encapsulates the scent of total calm and gentle stillness with its relaxing and calming properties. This includes a warm oil scalp treatment and massage followed by LaSultane’s foot spa ritual.<br><br>

8. RV Signature Lightening Hammam		<br><br>
An age-old Middle Eastern lightening ritual which is deeply exfoliating and intensely cleansing to rejuvenate, eliminate old skin and completely Renew Skin tone. Beginning with a black soap cleanse whilst in the hammam, before a full body traditional exfoliation with kessa mitt. After which a combination of fruit acids is applied to eat off the exfoliated skin at the cellular level. Then the signature blend of Rassoul Clay and Professional strength Kojic Acid is Applied to the skin to Lighten the new skin. 


